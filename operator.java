public class operator {
  public static void main(String[] args) {
    //declare variables
    int a = 100, b = 10;
    //additional operator
    System.out.println("a + b =" + (a+b));
   //subtraction operator
     System.out.println("a - b =" + (a-b));
     //multiplication operator
     System.out.println("a * b =" + (a*b));
     //division operator
     System.out.println("a / b =" + (a/b));
    //MODULO OPERATOR
    System.out.println("a % b=" + (a%b));
}
}