public class assoperator {
    public static void main(String[] args) {
        int a = 5;
        int var;
        //assign value using =
        var = a;
        System.err.println("varusing =" + var);
        //assign value using =+
        var += a;
        System.out.println("varusing =" + var);
        //assign value using *=
        var *= a;
        System.out.println("varusing =" + var);
        //assign value using /=
        var /= a;
        System.out.println("varusing =" + var);
    }
}
